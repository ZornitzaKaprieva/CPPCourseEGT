/*7. Error Handling:
Describe how exceptions are used in C++ for error handling, with an example of try-catch block.

Answer (EN):  
Exceptions in C++ are used to ensure that various unexpected errors will not prevent the program from executing correctly.
The try-catch block is such a block of code. It manages to catch the error and specify the program how to handle it. 
A good example of this is division by 0. 
If during the program there is a command to divide a number by 0, the try-catch block catches this and outputs an error message or finds some other way to handle the error. 

Answer (BG): 
Exceptions в С++ се използват, за да се осигури сигурност, че различни непредвидени грешки няма да попречат на правилното изпълнение на програмата. 
Try-catch block е такъв вид конструкция. Тя успява да улови грешката и да посочи на програмата как да се справи с нея . 
Добър пример за това е делението на 0. 
Ако по време на програмата има команда дадено число да се радели на 0, то try-catch block улавя това и извежда съобщение за грешка или намира друг начин да се справи с грешката.*/